package khanhcn.duanquanlytiendien.entity;

public class Phuong {
	private int id;
	private String ten;
	
	public Phuong(int id, String ten) {
		super();
		this.id = id;
		this.ten = ten;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTen() {
		return ten;
	}

	public void setTen(String ten) {
		this.ten = ten;
	}


}
